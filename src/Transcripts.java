private void waitfunction(int s, int n){
        Cursor waitCursor = new Cursor(Cursor.WAIT_CURSOR);
        Cursor defaultCursor = new Cursor(Cursor.DEFAULT_CURSOR);
        
        switch (s){
           case 1:
                try 
                {
                    jButton4.setCursor(waitCursor);
                    Thread.sleep(n);
                    jButton4.setCursor(defaultCursor);
                } 
                catch(InterruptedException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
           case 2:
                try 
                {
                    jButton7.setCursor(waitCursor);
                    Thread.sleep(n);
                    jButton7.setCursor(defaultCursor);
                } 
                catch(InterruptedException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
           case 3:
                try 
                {
                    jButton8.setCursor(waitCursor);
                    Thread.sleep(n);
                    jButton8.setCursor(defaultCursor);
                } 
                catch(InterruptedException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
           case 4:
                try 
                {
                    jButton6.setCursor(waitCursor);
                    Thread.sleep(n);
                    jButton6.setCursor(defaultCursor);
                } 
                catch(InterruptedException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
        }
}

