    public boolean checkdata(int i)
    {
        boolean temp = false;
            switch(i){
                case 1:   
                    if(jTextField6.getText().equals("")||jTextField7.getText().equals("")||
                       jTextField8.getText().equals("")||jTextField9.getText().equals("")||
                       jTextField10.getText().equals(""))
                        temp=false;
                    else
                        temp=true;
                    if(jTextField6.getText().equals(""))
                        jTextField6.setBackground(Color.red);
                    else
                        jTextField6.setBackground(Color.white);
                    if(jTextField7.getText().equals(""))
                        jTextField7.setBackground(Color.red);
                    else
                        jTextField7.setBackground(Color.white);
                    if(jTextField8.getText().equals(""))
                        jTextField8.setBackground(Color.red);
                    else
                        jTextField8.setBackground(Color.white);
                    if(jTextField9.getText().equals(""))
                        jTextField9.setBackground(Color.red);
                    else
                        jTextField9.setBackground(Color.white);
                    if(jTextField10.getText().equals(""))
                        jTextField10.setBackground(Color.red);
                    else
                        jTextField10.setBackground(Color.white);
                    break;
                case 2:
                    if(jTextField1.getText().equals("")||jTextField3.getText().equals("")||
                       jTextField4.getText().equals("")||jComboBox1.getSelectedIndex()==0||
                       jComboBox3.getSelectedIndex()==0||jComboBox4.getSelectedIndex()==0)
                        temp=false;
                    else
                        temp=true;
                    if(jTextField1.getText().equals(""))
                        jTextField1.setBackground(Color.red);
                    else
                        jTextField1.setBackground(Color.white);
                    if(jTextField3.getText().equals(""))
                        jTextField3.setBackground(Color.red);
                    else
                        jTextField3.setBackground(Color.white);
                    if(jTextField4.getText().equals(""))
                        jTextField4.setBackground(Color.red);
                    else
                        jTextField4.setBackground(Color.white);

                    if(jComboBox1.getSelectedIndex()==0)
                        jComboBox1.setBackground(Color.red);
                    else
                        jComboBox1.setBackground(backcolor);
                    if(jTextField2.getText().equals(""))
                        jTextField2.setBackground(Color.red);
                    else
                        jTextField2.setBackground(Color.white);
                    if(jComboBox3.getSelectedIndex()==0)
                        jComboBox3.setBackground(Color.red);
                    else
                        jComboBox3.setBackground(backcolor);
                    if(jComboBox4.getSelectedIndex()==0)
                        jComboBox4.setBackground(Color.red);
                    else
                        jComboBox4.setBackground(backcolor);
                    break;
                case 3:
                    if(jTextField5.getText().equals("")||jTextField11.getText().equals("")||
                       jTextField12.getText().equals(""))
                        temp=false;
                    else
                        temp=true;
                    if(jTextField5.getText().equals(""))
                        jTextField5.setBackground(Color.red);
                    else
                        jTextField5.setBackground(Color.white);
                    if(jTextField11.getText().equals(""))
                        jTextField11.setBackground(Color.red);
                    else
                        jTextField11.setBackground(Color.white);
                    if(jTextField12.getText().equals(""))
                        jTextField12.setBackground(Color.red);
                    else
                        jTextField12.setBackground(Color.white);
                    break;                  
            }
            return temp;
    }
